                    <p>Unicircle has a No-tolerance policy for any sort of abuse, harassment & bullying on its platform, including any behaviour that prevents constructive communication. Unicircle's anti-bullying practices are listed below to help guide students, teachers and administrators if they see or experience bullying on Unicircle.</p>
                    
                    <h2>All communication is controlled by the Users</h2>
                    <p>Students and Teachers on Unicircle can leave a group, turn off replies in a conversation or block & report another user.</p>
                    <h2>Users can Report bullying communication or situations.</h2>
                    <p>We strongly ask users to actively report any abuse, harassment, or bullying that they see or experience on Unicircle. Bullying can include:</p>
                    <ul>
                        <li>Threats of violence</li>
                        <li>Malicious Words</li>
                        <li>Rumours</li>
                    </ul>
                    <p>Reported users may receive a ban or a warning and will be prohibited from further communicating on Unicircle. The unicircle team may also decide to delete or temporarily block the account of a user who participates in an act of bullying or any other harmful behaviours.</p>
                    <h2>Messages are permanently recorded.</h2>
                    <p>Every communication sent on Unicircle is recorded unless the law requires us to delete the information. This allows our team, in collaboration with administrators and teachers, to recognise when harassment, bullying, or abuse occurs on Unicircle. Communication cannot be edited or deleted by students after they’re sent.</p>
                    <h2>Safety is a shared responsibility.</h2>
                    <p>We ask everyone in the Unicircle community to be aware of and take action against bullying. However, it’s not easy to monitor every space in a campus community but working together will help keep communication focused on engagement and learning.</p>
