                            on campus)</p>
                        <p>Student portals provide highly-personalized information that is extended to mobile push
                            notifications to create highly-tailored communication strategies. Many colleges face
                            communication challenges because they don't have a mobile app or aren't using customizable
                            push notifications.</p>
