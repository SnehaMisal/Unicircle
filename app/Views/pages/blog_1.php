                                    have
                                    difficulty using computers.
                                </p>
                                <p>While campus closings may have exacerbated the problem in 2020, part-time and remote
                                    learners
                                    may still experience difficulties accessing on-campus computers during operating
                                    hours, even
                                    when the university is open for business.</p>
                                <p>All students have access to the same features, information, and tools through the
                                    student
                                    portal mobile app via the traditional web portal. It could be their only means of
                                    accessing
                                    community and assistance in many cases.</p>
                                <p>There is no question that universities need to provide students with options and make
                                    it easy
                                    for them to participate from various devices. It's not realistic to expect students
                                    to be
