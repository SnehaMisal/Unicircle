                    <h2 class="desktop-display">Community Guidelines</h2>
                    <p>Unicircle is a campus app that helps students, administration staff and teachers to communicate,
                        share, and engage with one another. By connecting everyone in an Institution, Unicircle helps
                        educators and organizers build community both within and out of the campus</p>
                    <p>Respect & Trust are at the centre of any meaningful community, and the students well-being is our
                        highest priority. We understand that safety is a shared responsibility and everyone using the
                        platform plays an major role in keeping the Unicircle community safe.</p>
                    <p>We strongly encourage all users—whether you're an <b> organizer</b> (e.g., teacher,
                        administrator, coach), <b> participant</b> (e.g., student, alumnus), or <b>organization</b>
                        (e.g., high school, university)—to report every inappropriate contact, content, or use of
                        Unicircle's services.</p>
                    <h2>As part of the Unicircle community, keep the following in mind:</h2>
                    <h2>Keep communication constructive.</h2>
                    <p>Communication should be appropriate for recipients of all age levels and constructive in nature,
                        so we encourage you to carefully consider all message content before sending.</p>
                    <p>Communications on Unicircle can't be edited or deleted. Verified administrators at your
                        institution can view your engagements and conversations.</p>
                    <h2>Connect with people you know.</h2>
                    <p>Before you join a group or accept an invitation, take a second to make sure you recognize the
                        admin or teacher. If you accept an invitation from a student or group by mistake, you can leave
                        at any time.</p>
                    <h2>Send useful, relevant messages.</h2>
                    <p>All though each community is different, each communication you send should be valuable for your
                        participants. Listen to feedback about the relevance, and length of your messages. If your
                        messages are important for only a small group in your institute, for example, consider creating
                        a persona or a group conversation.</p>
                    <h2>Help keep our community safe by avoiding the following:</h2>
                    <h2>Profanity.</h2>
                    <p>Don't use phrases and words that you wouldn't say in front of your parents or over the college
                        loudspeaker. Keep it clean. We built Unicircle first and foremost for education.</p>
                    <h2>Nudity or sexual content of any kind.</h2>
                    <p>There's no place for sexually suggestive or explicit content on Unicircle, including nudity of
                        any kind. If we find that if any of your interactions on the platform include this content, your
                        account will be blocked/deleted.</p>
                    <h2>Harassment, bullying, and gossip.</h2>
                    <p>Messages meant to speak maliciously about other people, harass, or bully are unacceptable. You
                        can report anyone who sends messages of this nature. We also encourage you to seek out resources
                        or contact a trusted adult if you or someone you know is being bullied or feels unsafe.</p>
                    <h2>Pretending to be someone else.</h2>
                    <p>Make sure you are using your real name and select the accurate role. Impersonating someone else
                        or Misrepresenting yourself is not allowed on Unicircle.</p>
                    <h2>Spamming participants.</h2>
                    <p>Consider the frequency and content of your messages. Unicircle prohibits sending multiple
                        messages a day without regard for your participants or messages with irrelevant content. If we
                        detect behaviour that resembles spamming, your account may be deleted. </p>
                    <h2>Threatening harm to yourself or others.</h2>
                    <p>Any communication that threatens harm to yourself or others goes against our rules to keep our
                        community safe. If you're considering any harm upon yourself, please seek out resources and
                        contact a trusted adult. And if anyone is sending violent threats, please report that person on
                        Unicircle and contact a trusted adult.</p>
                    <h2>Uses outside the educational space.</h2>
                    <p>Communication on Unicircle should be used to support learning, and features are intended for
                        educational uses only. Unicircle should not be used for personal profit or commercial purposes.
                    </p>
                    <p>Messages on Unicircle are monitored for these guidelines, and users with inappropriate content
                        may be deactivated, blocked, or reported to the law enforcement agencies. Review our <a href="http://">Terms of
                        Service</a> and <a href="<?php echo base_url('privacy_policy')?>"> Privacy Policy</a> as well—and if you have any questions, send an email to our Support
                        team at <a href="mailto:support@unicircle.io">support@unicircle.in</a></p>
                    <p>Thanks for working with us to keep the Unicircle community safe and supportive!</p>
