                            people. Smartphones are the main thing that most college students have. A survey showed that
                            around 96% of adults between the ages of 18 and 29 have a smartphone. Most colleges have
                            started to embrace being mobile-first or adapting digital transformations, so colleges must
                            have a mobile app for the student portal. Mobile-first means that colleges will begin
                            personalizing how students can engage with the world through technology and not just an app
                            that can be downloaded and used.

                        </p>
                        <p>Mobile strategies that work flip the script from requiring students to search for information
                            to present them with tailored information. According to a Salesforce report, most students
                            would like to receive as many alerts, emails, personalized messages, and reminders as they
                            do now. In an unmistakable message, students want direct access to personalized assistance.
                        </p>
